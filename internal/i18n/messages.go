func SupportedLocales() []string {
	return []string{"en", "it", "es", "de", "fr"}
}

func ResolveLocale(acceptLanguage string) string {
	if acceptLanguage == "" {
		return DefaultLocale
	}
	type langQ struct {
		lang string
		q    float64
	}
	candidates := make([]langQ, 0, 8)
	for _, part := range strings.Split(acceptLanguage, ",") {
		p := strings.TrimSpace(part)
		if p == "" {
			continue
		}
		lang := p
		q := 1.0
		if strings.Contains(p, ";") {
			chunks := strings.Split(p, ";")
			lang = strings.TrimSpace(chunks[0])
			for _, c := range chunks[1:] {
				c = strings.TrimSpace(c)
				if strings.HasPrefix(c, "q=") {
					if parsed, err := strconv.ParseFloat(strings.TrimPrefix(c, "q="), 64); err == nil {
						q = parsed
					}
				}
			}
		}
		lang = NormalizeLocale(lang)
		if lang == "" {
			continue
		}
		candidates = append(candidates, langQ{lang: lang, q: q})
	}

	best := DefaultLocale
	bestQ := -1.0
	for _, c := range candidates {
		if _, ok := supported[c.lang]; ok && c.q > bestQ {
			best = c.lang
			bestQ = c.q
		}
	}
	return best
}

func NormalizeLocale(locale string) string {
	locale = strings.ToLower(strings.TrimSpace(locale))
	if locale == "" {
		return ""
	}
	if strings.Contains(locale, "-") {
		locale = strings.SplitN(locale, "-", 2)[0]
	}
	if strings.Contains(locale, "_") {
		locale = strings.SplitN(locale, "_", 2)[0]
	}
	return locale
}

func T(locale, key string, args ...any) string {
	locale = NormalizeLocale(locale)
	if _, ok := supported[locale]; !ok {
		locale = DefaultLocale
	}
	msg, ok := messages[locale][key]
	if !ok {
		msg = messages[DefaultLocale][key]
	}
	if msg == "" {
		msg = key
	}
	if len(args) > 0 {
		return fmt.Sprintf(msg, args...)
	}
	return msg
}

func DaysLeft(locale string, t time.Time) string {
	d := time.Until(t)
	if d <= 0 {
		return T(locale, "time.expired")
	}
	if d < 24*time.Hour {
		hours := int(d.Hours())
		if hours < 1 {
			hours = 1
		}
		return fmt.Sprintf("%dh", hours)
	}
	days := int(d.Hours() / 24)
	if days == 1 {
		return T(locale, "time.one_day")
	}
	return T(locale, "time.days", days)
}

func DaysAgo(locale string, t time.Time) string {
	days := int(time.Since(t).Hours() / 24)
	if days <= 0 {
		return T(locale, "time.today")
	}
	if days == 1 {
		return T(locale, "time.yesterday")
	}
	return T(locale, "time.days_ago", days)
}
